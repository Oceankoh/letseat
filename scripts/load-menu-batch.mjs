#!/usr/bin/env node

import { createHash } from "node:crypto";
import { readFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const DEFAULT_FILES = [
  "data/menu-research/manual-batch-1.json",
  "data/menu-research/batch-3.json"
];

function slugify(value) {
  return String(value || "value")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .slice(0, 64) || "value";
}

function shortHash(value) {
  return createHash("sha256").update(String(value)).digest("hex").slice(0, 10);
}

function sqlString(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlTextArray(values) {
  if (!Array.isArray(values) || !values.length) return "'{}'::text[]";
  return `ARRAY[${values.map(sqlString).join(", ")}]::text[]`;
}

function sourceType(value) {
  const normalized = String(value || "").toLowerCase();
  if (["restaurant_website", "official_website", "restaurant_website_pdf"].includes(normalized)) return "restaurant_website";
  if (normalized.includes("partner")) return "partner";
  if (normalized.includes("api")) return "approved_api";
  return "manual";
}

function itemPrice(item) {
  if (item.price !== null && item.price !== undefined) return Number(item.price);
  if (item.price_cents !== null && item.price_cents !== undefined) {
    return Number(item.price_cents) / 100;
  }
  return null;
}

function normalizeMenu(menu) {
  const placeKey = menu.place_id || `name:${menu.place_name}`;
  const menuId = `menu_${slugify(menu.place_id || menu.place_name)}_${shortHash(`${menu.source_url}:${menu.captured_at}`)}`;
  const capturedAt = menu.captured_at?.includes("T") ? menu.captured_at : `${menu.captured_at || "2026-05-03"}T12:00:00+08:00`;
  return {
    ...menu,
    place_key: placeKey,
    menu_id: menuId,
    source_type_sql: sourceType(menu.source_type),
    captured_at_sql: capturedAt,
    items: (menu.items || []).filter((item) => item?.name).map((item, index) => ({
      ...item,
      id: `item_${slugify(menu.place_id || menu.place_name)}_${slugify(item.name)}_${index + 1}`,
      normalized_name: String(item.name).toLowerCase().replace(/[^a-z0-9\s]/g, " ").replace(/\s+/g, " ").trim(),
      category: item.category || "Menu",
      price: itemPrice(item),
      currency: item.currency || "SGD",
      confidence: Number(item.confidence ?? menu.confidence ?? 0.7)
    }))
  };
}

function buildSql(menus) {
  const lines = [
    "-- Generated by scripts/load-menu-batch.mjs",
    "BEGIN;"
  ];

  for (const menu of menus) {
    const placeWhere = menu.place_id
      ? `id = ${sqlString(menu.place_id)}`
      : `lower(name) = lower(${sqlString(menu.place_name)})`;

    lines.push(`
DO $$
DECLARE
  target_place_id text;
BEGIN
  SELECT id INTO target_place_id
  FROM places
  WHERE ${placeWhere}
  ORDER BY review_count DESC NULLS LAST
  LIMIT 1;

  IF target_place_id IS NULL THEN
    RAISE NOTICE 'Skipping menu for %, no matching place found', ${sqlString(menu.place_name)};
  ELSE
    DELETE FROM menu_items WHERE place_id = target_place_id;
    DELETE FROM menus WHERE place_id = target_place_id;

    INSERT INTO menus (
      id,
      place_id,
      source_type,
      source_url,
      captured_at,
      license_status,
      processing_status,
      is_active,
      raw_ocr_text
    ) VALUES (
      ${sqlString(menu.menu_id)},
      target_place_id,
      ${sqlString(menu.source_type_sql)},
      ${sqlString(menu.source_url)},
      ${sqlString(menu.captured_at_sql)},
      ${sqlString(menu.source_type_sql === "restaurant_website" ? "restaurant_owned" : "unknown")},
      'approved_active',
      true,
      ${sqlString(menu.notes || null)}
    )
    ON CONFLICT (id) DO NOTHING;
`);

    for (const item of menu.items) {
      lines.push(`
    INSERT INTO menu_items (
      id,
      place_id,
      menu_id,
      name,
      normalized_name,
      description,
      category,
      price,
      currency,
      dietary_tags,
      ocr_confidence,
      extraction_confidence,
      source_text,
      is_available
    ) VALUES (
      ${sqlString(item.id)},
      target_place_id,
      ${sqlString(menu.menu_id)},
      ${sqlString(item.name)},
      ${sqlString(item.normalized_name)},
      ${sqlString(item.description || null)},
      ${sqlString(item.category)},
      ${item.price === null || item.price === undefined ? "NULL" : Number(item.price)},
      ${sqlString(item.currency)},
      ${sqlTextArray(item.dietary_tags)},
      NULL,
      ${Math.max(0, Math.min(1, item.confidence))},
      ${sqlString(menu.source_url)},
      true
    )
    ON CONFLICT (id) DO UPDATE SET
      name = EXCLUDED.name,
      normalized_name = EXCLUDED.normalized_name,
      description = EXCLUDED.description,
      category = EXCLUDED.category,
      price = EXCLUDED.price,
      currency = EXCLUDED.currency,
      dietary_tags = EXCLUDED.dietary_tags,
      extraction_confidence = EXCLUDED.extraction_confidence,
      source_text = EXCLUDED.source_text,
      is_available = true,
      updated_at = now();`);
    }

    lines.push(`
  END IF;
END $$;`);
  }

  lines.push("COMMIT;");
  return `${lines.join("\n")}\n`;
}

async function main() {
  const files = process.argv.slice(2).length ? process.argv.slice(2) : DEFAULT_FILES;
  const menusByPlace = new Map();

  for (const file of files) {
    let parsed;
    try {
      parsed = JSON.parse(await readFile(path.join(process.cwd(), file), "utf8"));
    } catch {
      continue;
    }
    for (const menu of parsed) {
      const normalized = normalizeMenu(menu);
      const existing = menusByPlace.get(normalized.place_key);
      if (!existing || normalized.confidence >= existing.confidence) {
        menusByPlace.set(normalized.place_key, normalized);
      }
    }
  }

  const menus = Array.from(menusByPlace.values());
  const sql = buildSql(menus);
  process.stdout.write(sql);
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
